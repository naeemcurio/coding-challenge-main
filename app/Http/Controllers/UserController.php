        $sendRequests = Auth::user()->sendRequests()->pluck('receiver_id');
        $receivedRequests = Auth::user()->receivedRequests()->pluck('sender_id');
        return User::whereNotIn('id', [...$connections, ...$sendRequests, ...$receivedRequests])->paginate(10);

